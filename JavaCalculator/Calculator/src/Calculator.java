    private void calculate(int count){
        if(count == 1){
           // MINUS OPRATION
           ans = num1 - Float.parseFloat(this.txtDisplay.getText());
           this.txtDisplay.setText(String.valueOf(ans));
        }
        else if(count == 2){
            // PlUS OPRATION
            ans = num1 + Float.parseFloat(this.txtDisplay.getText());
            this.txtDisplay.setText(String.valueOf(ans));
        }
        else if(count == 3){
            // MULTIPICATION OPERATION
             ans = num1 * Float.parseFloat(this.txtDisplay.getText());
            this.txtDisplay.setText(String.valueOf(ans));
        }
        else if(count == 4){
            // DIVITION OPRATION
             ans = num1 / Float.parseFloat(this.txtDisplay.getText());
            this.txtDisplay.setText(String.valueOf(ans));
        }
    }
    
