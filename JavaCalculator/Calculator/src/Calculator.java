    private void cmbTheamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbTheamActionPerformed
     
        if(this.cmbTheam.getSelectedIndex() == 0){
            // DARK MOOD: 
            this.pnlBackground.setBackground(Color.DARK_GRAY);
            this.txtDisplay.setBackground(Color.WHITE);
            this.pnlbtn.setBackground(Color.DARK_GRAY);
            this.txtDisplay.setForeground(Color.BLUE);
            
        }
        else if(this.cmbTheam.getSelectedIndex()== 1){
            // LIGHT MOOD : 
            this.pnlBackground.setBackground(Color.WHITE);
            this.txtDisplay.setBackground(Color.DARK_GRAY);
            this.pnlbtn.setBackground(Color.LIGHT_GRAY);
            this.txtDisplay.setForeground(Color.cyan);
        }
    }//GEN-LAST:event_cmbTheamActionPerformed

